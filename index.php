                <br>
                <p>CYFLA membership is open to alumni of PYPA and other people who meet its membership criteria. Its current affliation reflects political parties represented in Parliament. Its members consist of parties represented in Parliament namely Aliance for Democracy (AFORD), Democratic Progressive Party (DPP), Malawi Congress Party (MCP), People's Party (PP) and United Democratic Front (UDF). However, Membership is open even to members of other active and formally structured political parties whether the parties are represented  in Parliament or not.
                </p>
