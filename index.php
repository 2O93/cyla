               <div class="aboutright">
                  <h4> What Are We</h4>
               </div>
               <div class="about-jst-right">
                  <p>Centre for Young Leaders in Africa (CYLA) is a non profit making organisation which was founded by young men and women from different political parties in Malawi. It was established in 2016 by young men and women who were trained under the <a href="#" style="color:blue;">Programme for Young Leaders in Africa (PYPA)</a> It was incorporated as a company limited by guarantee under the Companies Act no. 15 of 2017 in 2017. It was also registered as a non-governmental organisation under thr NGO Act no. 3 of 2001 in June 2017.
                  </p>
                  <p>The organisation aims at grooming a cadre of vibrant young political leaders. It endeavours to promote young people's active political participation, human rights, good governance, inter-intra  partner democracy, gender equality and equity for a better and sustainable social-economic development of all people in Malawi. Its stakeholders include the political party youth  directorates, politically active young male and female leaders, elected young leaders, women, policy makers and the media.  
                  </p>
               </div>
            </div>
